import Foundation

class Math {
    
    func add(_ x: Int, _ y: Int) -> Int {
        return x + y
    }
    
    func sub(_ x: Int, _ y: Int) -> Int {
        return x - y
    }
    
    func mul(_ x: Int, _ y: Int) -> Int {
        return x * y
    }
    
    func div(_ x: Double, _ y: Double) -> Double {
        return x / y
    }
}
